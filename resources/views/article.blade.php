        <div class="article-content">
            <p>Sevilla have opened formal talks over a permanent transfer for their on-loan winger, with the club keen to avoid a repeat of previous windows where impressive loan spells ended without a lasting deal.</p>

            <p>The 22-year-old joined in January on a short-term arrangement and has since become one of the most productive attacking players in the squad, contributing eight goals and five assists across nineteen appearances in all competitions.</p>

            <h3>A deal built around a release clause</h3>
            <p>Sporting sources close to the negotiation say the two clubs had already discussed an option-to-buy clause as part of the original loan agreement, which softens the financial burden Sevilla would otherwise face in a single summer window.</p>

            <div class="pull-quote">He has given us something we were missing in the final third, and everyone at the club wants to find a way to keep him.</div>

            <p>The player himself has spoken positively about his time at the club in recent interviews, describing the fanbase and the coaching staff as central to his rapid improvement in front of goal this season.</p>

            <h3>What happens next</h3>
            <p>Both parties are expected to continue discussions through July, with Sevilla hoping to have an agreement in place before pre-season training begins. Rival interest from two other clubs in Spain and Italy could complicate matters if talks stall.</p>

