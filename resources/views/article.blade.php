    <h1>Sevilla open talks over a permanent deal for their on-loan winger</h1>
    <p class="dek">The 22-year-old has scored eight goals since arriving in January, and the club now hopes to make the move permanent before he returns to his parent club this summer.</p>
    <div class="byline">
      <span>By Marta Ibáñez</span>
      <span>·</span>
      <span>Published July 4, 2026</span>
      <span>·</span>
      <span>5 min read</span>
    </div>
